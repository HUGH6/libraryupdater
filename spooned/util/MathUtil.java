package util;

public class MathUtil {
    public static int sum(int a, int b) {
        int c = a;
        int d = b;
        int e = c + d;
        return e;
    }

    public static int sum2(int a, int b) {
        return b + a;
    }

    public static int towTimes(int a) {
        return a + a;
    }
}